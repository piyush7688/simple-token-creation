let account;
let web3;
let contract;

async function connectWallet() {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        const accounts = await ethereum.request({ method: "eth_requestAccounts" });
        account = accounts[0];
        document.getElementById("walletAddress").innerText = `Connected: ${account}`;
        contract = new web3.eth.Contract(contractABI, contractAddress);
        updateBalance();
    } else {
        alert("Please install MetaMask.");
    }
}

async function updateBalance() {
    const balance = await contract.methods.balanceOf(account).call();
    document.getElementById("tokenBalance").innerText = balance;
}

async function transferTokens() {
    const to = document.getElementById("recipient").value;
    const amount = document.getElementById("amount").value;
    await contract.methods.transfer(to, amount).send({ from: account });
    updateBalance();
}

async function mintTokens() {
    const amount = document.getElementById("mintAmount").value;
    await contract.methods.mint(amount).send({ from: account });
    updateBalance();
}

async function burnTokens() {
    const amount = document.getElementById("burnAmount").value;
    await contract.methods.burn(amount).send({ from: account });
    updateBalance();
}

async function approveTokens() {
    const spender = document.getElementById("spender").value;
    const amount = document.getElementById("approveAmount").value;
    await contract.methods.approve(spender, amount).send({ from: account });
}

async function transferFromTokens() {
    const from = document.getElementById("fromAddress").value;
    const to = document.getElementById("toAddress").value;
    const amount = document.getElementById("transferFromAmount").value;
    await contract.methods.transferFrom(from, to, amount).send({ from: account });
}

document.getElementById("connectWallet").addEventListener("click", connectWallet);
